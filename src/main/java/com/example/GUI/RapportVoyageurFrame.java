    private void ChercherParID() throws SQLException {
        VoyageurUtil vu = new VoyageurUtil();
        LinkedList<Voyageur> trajets = new LinkedList<>();
        trajets = vu.rechercherParID(Integer.parseInt(cIDv.getText()));
        DefaultTableModel tablemodeleModele = new DefaultTableModel();
        String[][] tableTrajets;
        tablemodeleModele = (DefaultTableModel) tabletrajets.getModel();
        tableTrajets = new String[trajets.size()][5];

        for (int i = 0; i < trajets.size(); i++) {
            tableTrajets[i][0] = Integer.toString(trajets.get(i).getId());
            tableTrajets[i][1] = trajets.get(i).getNom();
            tableTrajets[i][2] = trajets.get(i).getPrenom();
            tableTrajets[i][3] = trajets.get(i).getEmail();
            tableTrajets[i][4] = Integer.toString(trajets.get(i).getIdReduction());


            tablemodeleModele.addRow(tableTrajets[i]);
        }
    }
